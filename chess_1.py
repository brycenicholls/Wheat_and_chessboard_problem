def add(x, y):
    """add"""
    return x + y


def sub(x, y):
    """subtract"""
    return x - y


def div(x, y):
    """divide"""
    return x / y


def mult(x, y):
    """multiply"""
    return x * y


def mod(x, y):
    """remainder"""
    return x % y


def power(x, y):
    """exponentiation"""
    return x ** y

